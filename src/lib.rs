/// Adds two numbers
///
/// # Examples
///
/// ```
/// use conventional_commit_example::add;
///
/// let result = add(2, 3);
/// assert_eq!(result, 5);
/// ```
pub fn add(a: i32, b: i32) -> i32 {
    a + b
}

/// Subtract two numbers
///
/// # Examples
///
/// ```
/// use conventional_commit_example::subtract;
///
/// let result = subtract(3, 2);
/// assert_eq!(result, 1);
/// ```
pub fn subtract(a: i32, b: i32) -> i32 {
    a - b
}
